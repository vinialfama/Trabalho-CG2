package libgdx.revolutiondancers.engine;



/**
 *
 * @author Vinícius
 */
public class Ligacoes {
    private int x;
    private int y;
    
    
    public Ligacoes(int x, int y){
        this.x = x;
        this.y = y;
    }
    
    public int getX(){
        return this.x;
    }
    
    public int getY(){
        return this.y;
    }
}
